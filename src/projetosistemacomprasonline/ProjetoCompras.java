    private void jbAlterarPedidosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbAlterarPedidosActionPerformed
        // TODO add your handling code here:
        
         InitialContext ctx;
         Pedidos ped= new Pedidos();
         ped.setId(Long.parseLong(txtIDPedido.getText()));
         ped.setFilialentrega(String.valueOf(jcFilial.getSelectedItem()));

            try {
            ctx = new InitialContext();

            
            InterfacePedidosRemote pedidorem = (InterfacePedidosRemote) ctx.lookup("java:global/ProjetoSistemaComprasOnline/ProjetoSistemaComprasOnline-ejb/ClassePedidoRe");
            ped= pedidorem.consultarPedidosFilial(ped);
            
            if(ped!=null){
                
                if(jcStatus.getSelectedItem().equals("ENTREGUE"))
                    {
                    
                    Produtos prod = new Produtos();
                    InterfaceProdutosRemote prodrem = (InterfaceProdutosRemote) ctx.lookup("java:global/ProjetoSistemaComprasOnline/ProjetoSistemaComprasOnline-ejb/ClasseProdutoRe");

                    prod.setId(Long.parseLong(ped.getIdProduto()));
                    int quant1= ped.getQuantidade();
                    prod = prodrem.consultar(prod);
                    if(ped.getFilialentrega().equals("FILIAL1")){
                    if(prod.getEstoque1()>= quant1){
                       int temp= prod.getEstoque1()-quant1;
                       prod.setEstoque1(temp);
                       prodrem.alterar(prod);
                       ped.setStatus((String.valueOf(jcStatus.getSelectedItem())));
                       pedidorem.alterar(ped);
                       JOptionPane.showMessageDialog(this, "Alterado com sucesso!!");
                       txtIDPedido.setText("");
                       txtStatusAtual.setText("");
                       jcStatus.setSelectedItem("Selecione");
                    }
                    else{
                        JOptionPane.showMessageDialog(this, "Quantidade insuficiente no estoque!!");
                        txtIDPedido.setText("");
                       txtStatusAtual.setText("");
                    }

                    }
                    else{
                        int temp= prod.getEstoque2()-quant1;
                       prod.setEstoque2(temp);
                       prodrem.alterar(prod);
                       ped.setStatus((String.valueOf(jcStatus.getSelectedItem())));
                       pedidorem.alterar(ped);
                       JOptionPane.showMessageDialog(this, "Alterado com sucesso!!");
                       txtIDPedido.setText("");
                       txtStatusAtual.setText("");
                       jcStatus.setSelectedItem("Selecione");
                    }
                    
                    }
                else{
                    ped.setStatus((String.valueOf(jcStatus.getSelectedItem())));
                    pedidorem.alterar(ped);
                    JOptionPane.showMessageDialog(this, "Alterado com sucesso!!");
                    txtIDPedido.setText("");
                    txtStatusAtual.setText("");
                    jcStatus.setSelectedItem("Selecione");
                }
               
            }
            
            else{
                 JOptionPane.showMessageDialog(this, "Pedido não existe");
            }
            }catch (NamingException ex) {
                System.out.println("Erro de Transação");
                Logger.getLogger(ProjetoCompras.class.getName()).log(Level.SEVERE, null, ex);
            }
    }//GEN-LAST:event_jbAlterarPedidosActionPerformed

    
                                             
        

   
