    private void jbCadastrarVendasActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbCadastrarVendasActionPerformed
        // TODO add your handling code here:
        Vendas ven = new Vendas();
        Vendas ven2 = new Vendas();
        
         int dia;
        dia = Integer.parseInt(txtdiav.getText());
        int mes = Integer.parseInt(txtmesv.getText());
        int ano = Integer.parseInt(txtanov.getText());
        InitialContext ctx;
        
        try {
            ctx = new InitialContext();

            InterfaceVendasRemote venrem = (InterfaceVendasRemote) ctx.lookup("java:global/ProjetoSistemaComprasOnline/ProjetoSistemaComprasOnline-ejb/ClasseVendas");
           
            
            ven.setDia(dia);
            ven.setMes(mes);
            ven.setAno(ano);
            ven.setQuant(Integer.parseInt(txtQuant1.getText())+ Integer.parseInt(txtQuant2.getText())+ Integer.parseInt(txtQuant3.getText()));
            ven.setValor(Double.parseDouble(txtValorTotal.getText()));

            if (venrem.consultarVendas(ven) == null) {
                venrem.incluir(ven);

                JOptionPane.showMessageDialog(this, "Venda Cadastrada com Sucesso");
                ProjetoCompras proj = new ProjetoCompras();
                proj.setVisible(true);
                dispose();
            } else {
                ven2= venrem.consultarVendas(ven);
                int quanttemp= ven2.getQuant()+ven.getQuant();
                double valortemp = ven2.getValor()+ven.getValor();
                ven2.setQuant(quanttemp);
                ven2.setValor(valortemp);
                venrem.alterar(ven2);

                JOptionPane.showMessageDialog(this, "Venda Cadastrada com Sucesso");

                ProjetoCompras proj = new ProjetoCompras();
                proj.setVisible(true);
                dispose();
            }
        } catch (NamingException ex) {
            System.out.println("Erro de Transação");
            Logger.getLogger(Cadastro.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        
        
        
        
        
        
    }//GEN-LAST:event_jbCadastrarVendasActionPerformed

